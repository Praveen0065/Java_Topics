	/*
	 
	 ============
	 HashMap
	 ============
	 -> It is implementation class for Map Interface
	 -> USed to store data in key value format
	 -> Default capacity is 16
	 -> Load factor 0.75
	 -> Underlying data Structure is HashTable
	 -> Insertion Order will not maintained by HashMap
	 
	 ============
	 LinkedHashMap
	 ============	 
	 -> Implementation class of Map interface
	 -> Maintains insertion order
	 -> Data Structure is HashTable + double LinkedList
	 
	 =========
	 TreeMap
	 =========	 
	 -> Implementation class of Map interface
	 -> It maintains sorted order(natural sorting order )
	 -> Internal Data Structure of the Tree Map is binary tree
	 
	 
	 ============
	 HashTable
	 ===========	 
	 -> Ii is implementation class of Map Interface
	 -> Default capacity is 11
	 -> Load factor 0.75
	 -> key-value format to store the data
	 -> HashTable is legacy class (jdk 1.0 v)
	 -> HashTable is synchronized
	 
	 -> If thread safety is now required then use HashMap instead of HashTable
	 -> If thread Safety is important then go for ConcurrentHashMap instead of HashTable
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 */
	
	
	
	
	
	
	
	
